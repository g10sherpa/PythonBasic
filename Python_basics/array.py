cars = ["Ford", "Volvo", "BMW"]
print(cars)

""".....Print first value...."""
x = cars[0]
print(x)

""".......Replace......."""
cars[0] = "Toyota"
print(cars)

"""....length....."""
x = len(cars)
print(x)

"""...For loops..."""
for x in cars:
      print(x)

""".....Append....."""
cars.append("Honda")
print(cars)


"""......Sort the list alphabetically......"""
cars = ['Ford', 'BMW', 'Volvo']
cars.sort()
print(cars)
